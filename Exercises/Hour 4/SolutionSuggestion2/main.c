/*  Given two numeric values, 72 and 104, write a program to print out the corresponding two characters.
*/
#include <stdio.h>
#include <stdlib.h>

int main()
{
    // By using the %c the later arguments are interpreted as characters
    printf("The numeric values 72 and 104 yields the corresponding characters: %c and %c respectively", 72, 104);
    return 0;
}
